// Create basic PNG assets using Node.js
const fs = require('fs');

// Minimal valid PNG file (1x1 transparent pixel)
const pngHeader = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR chunk length (13 bytes)
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0x01, // Width: 1
  0x00, 0x00, 0x00, 0x01, // Height: 1
  0x08, 0x06, 0x00, 0x00, 0x00, // Bit depth: 8, Color type: 6 (RGBA), Compression: 0, Filter: 0, Interlace: 0
  0x1F, 0x15, 0xC4, 0x89, // CRC32 of IHDR
  0x00, 0x00, 0x00, 0x0A, // IDAT chunk length (10 bytes)
  0x49, 0x44, 0x41, 0x54, // IDAT
  0x78, 0x9C, 0x62, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, // Compressed data
  0xE2, 0x21, 0xBC, 0x33, // CRC32 of IDAT
  0x00, 0x00, 0x00, 0x00, // IEND chunk length (0 bytes)
  0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82  // CRC32 of IEND
]);

// Create all required assets
fs.writeFileSync('icon.png', pngHeader);
fs.writeFileSync('adaptive-icon.png', pngHeader);
fs.writeFileSync('splash.png', pngHeader);

